<template>
  <v-card class="u-flex u-flex--column u-flex--center">
    <v-btn
      icon
      color="primary"
      @click="scrollUp"
    >
      <v-icon>keyboard_arrow_up</v-icon>
    </v-btn>

    <div
      class="sticky-container"
      :id="`table-container-${centerId}`"
      @scroll="onScroll"
    >
      <table>
        <thead>
          <tr>
            <th
              v-for="validDay in headers"
              :key="validDay.text"
              style="text-transform: uppercase;"
            >{{ validDay.text }}</th>
          </tr>
        </thead>

        <tbody ref="tableBody">
          <tr
            v-for="week in items"
            :key="week.numWeek"
            :id="`week-position-${week.numWeek}-${centerId}`"
            :class="`${currentWeek === week.numWeek ? 'blue lighten-4' : 'blue-grey lighten-5'}`"
          >
            <th :class="`th-ui-week-column ${setColorTh(week)}`">
              <v-layout row>
                <v-tooltip
                  bottom
                  lazy
                >
                  <template v-slot:activator="{ on }">
                    <div>
                      <strong v-on="on">{{ getStartOfWeek(week.numWeek) }}</strong>
                    </div>
                  </template>
                  <span>{{ $formatMessage({ id: 'date_first_day_of_week' }) }} {{ week.numWeek }}</span>
                </v-tooltip>

                <v-divider
                  vertical
                  style="margin: 0 1em;"
                ></v-divider>

                <v-tooltip
                  bottom
                  lazy
                >
                  <template v-slot:activator="{ on }">
                    <div v-on="on">
                      <strong>{{ week.bookedPlaces }}</strong>
                      &nbsp;
                      <strong>/</strong>
                      &nbsp;
                      <strong>{{ week.totalPlaces }}</strong>
                    </div>
                  </template>
                  <span>{{ $formatMessage({ id: 'total_of_week' }) }}</span>
                </v-tooltip>
              </v-layout>
            </th>
            <td
              v-for="(day, idx) in week.days"
              :key="`week-${day.numWeek}-day-${idx}`"
            >
              <v-btn @click="goToGestionPlannings(week.numWeek, idx + 1)">
                <span class="text-free-places">
                  <strong>{{ getCountBookedPlaces(day) }}</strong>
                </span>
                &nbsp;
                <strong>/</strong>
                &nbsp;
                <span>
                  <strong>{{ day.length }}</strong>
                </span>
              </v-btn>
            </td>
          </tr>
        </tbody>
      </table>
    </div>

    <div class="u-flex  u-flex--space-between  u-full-width">
      <div style="flex-basis: 4em;">&nbsp;</div>
      <div style="flex-grow: 1;">&nbsp;</div>

      <div>
        <v-btn
          icon
          color="primary"
          @click="scrollDown"
        >
          <v-icon>keyboard_arrow_down</v-icon>
        </v-btn>
      </div>

      <div style="flex-grow: 1;">&nbsp;</div>

      <div>
        <v-tooltip
          left
          lazy
          max-width="300"
        >
          <template v-slot:activator="{ on }">
            <div>
              <v-btn
                v-on="on"
                icon
                color="info"
              >
                <v-icon>info</v-icon>
              </v-btn>
            </div>
          </template>
          <v-chip
            class="tooltip"
            color="green lighten-2"
            text-color="black"
          >
            <strong>{{ $formatMessage({ id: 'booked_places_100_pourcent' }) }}</strong>
          </v-chip>
          <v-chip
            class="tooltip"
            color="orange darken-1"
            text-color="black"
          >
            <strong>{{ $formatMessage({ id: 'booked_places_more_than_50_pourcent' }) }}</strong>
          </v-chip>
          <v-chip
            class="tooltip"
            color="red lighten-1"
            text-color="black"
          >
            <strong>{{ $formatMessage({ id: 'booked_places_lower_than_50_pourcent' }) }}</strong>
          </v-chip>
        </v-tooltip>
      </div>

    </div>
  </v-card>
</template>

<script>
import { scroller } from 'vue-scrollto/src/scrollTo'

import {
  getFrenchFormattedDateFromObject,
  getFrenchLuxonCurrentDateTime,
  getFrenchWeeksInWeekYear,
  validDays,
} from '@/util'

const orangeColor = 'orange darken-1'
const greenColor = 'green lighten-2'
const redColor = 'red lighten-1'

export default {
  props: {
    items: Array,
    validDays: Array,
    centerId: String,
  },

  data () {
    return {
      currentSelectedWeek: getFrenchLuxonCurrentDateTime().weekNumber,
      scrollTo: scroller(),
      headers: [
        {
          text: 'Semaine du',
          align: 'left',
          sortable: false,
          width: '50',
        },
        ...validDays.map(el => ({ text: el, align: 'center', sortable: false })),
      ],
    }
  },

  computed: {
    activeDepartement () {
      return this.$store.state.admin.departements.active
    },

    currentWeek () {
      return getFrenchLuxonCurrentDateTime().weekNumber
    },

    scrollOptions () {
      if (this.centerId) {
        return {
          container: `#table-container-${this.centerId}`,
          force: true,
          cancelable: true,
          x: false,
          y: true,
          offset: -36,
          easing: 'ease-in',
          duration: 500,
        }
      }
      return {}
    },
  },

  watch: {
    items: {
      immediate: true,
      deep: true,
      handler (newValue) {
        this.currentSelectedWeek = this.currentWeek
        setTimeout(this.scrollToSelectedWeek.bind(this), 0)
      },
    },
  },

  methods: {
    getStartOfWeek (weekNumber) {
      const currentYear = getFrenchLuxonCurrentDateTime().weekYear
      const shape = {
        month: 'short',
        day: '2-digit',
        year: 'numeric',
      }
      return getFrenchFormattedDateFromObject(
        { weekYear: currentYear, weekNumber, weekday: 1 },
        shape
      )
    },

    goToGestionPlannings (weekNumber, idx) {
      this.$emit('goToGestionPlannings', weekNumber, idx)
    },

    getCountBookedPlaces (places) {
      const bookedPlacesCount = places.filter(elmt => elmt.candidat).length
      return bookedPlacesCount
    },

    onScroll (event) {
      const tableRowHeigth = this.$refs.tableBody.firstChild.clientHeight
      this.currentSelectedWeek = Math.floor(
        event.target.scrollTop / (tableRowHeigth || 48)
      )
    },

    setColorTh (week) {
      const { totalPlaces, bookedPlaces } = week
      if (
        totalPlaces &&
        totalPlaces !== bookedPlaces &&
        bookedPlaces >= totalPlaces / 2
      ) {
        return orangeColor
      }
      if (totalPlaces && totalPlaces === bookedPlaces) {
        return greenColor
      }
      if (totalPlaces) {
        return redColor
      }
      return ''
    },

    scrollUp () {
      this.currentSelectedWeek = Math.max(this.currentSelectedWeek - 1, 1)
      this.scrollToSelectedWeek()
    },

    scrollDown () {
      const weeksInWeekYear = getFrenchWeeksInWeekYear(
        getFrenchLuxonCurrentDateTime().year - 1
      )
      this.currentSelectedWeek = Math.min(
        this.currentSelectedWeek + 1,
        weeksInWeekYear - 1
      )
      this.scrollToSelectedWeek()
    },

    scrollToSelectedWeek () {
      this.scrollTo(
        `#week-position-${this.currentSelectedWeek}-${this.centerId}`,
        1,
        this.scrollOptions
      )
    },
  },
}
</script>

